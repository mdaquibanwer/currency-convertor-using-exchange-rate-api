const APIKey = "";  // Enter API KEY

const dropList = document.querySelectorAll(".drop-list select"),
fromCurrency = document.querySelector(".from select"),
toCurrency = document.querySelector(".to select"),
exchangeIcon = document.querySelector(".drop-list .icon"),
getBtn = document.querySelector("form button")

for(let i=0; i<dropList.length;i++){
    for(currencyCode in countryCode){
        let selectedCode;
        if(i === 0){
            selectedCode = currencyCode === "USD" ? "selected" : "";
        }
        else if(i === 1){
            selectedCode = currencyCode === "INR" ? "selected" : "";
        }
        let optionTag = `<option value="${currencyCode}"${selectedCode}>${currencyCode}</option>`
        dropList[i].insertAdjacentHTML("beforeend",optionTag);

    }
}
const getExchangeRate = ()=>{
    const amount = document.querySelector(".amount input");
    let amountVal = amount.value;
    if(amountVal === "" || amountVal === "0"){  // if user don't enetr any value or enter 0 then we will put value 1 there
        amount.value = "1";
        amountVal = 1;
    }
    let URL = `https://v6.exchangerate-api.com/v6/${APIKey}/latest/${fromCurrency.value}`;  // use own APIKEY
    fetch(URL).then(response => response.json()).then(result => {
        let exchangeRate = result.conversion_rates[toCurrency.value]
        // console.log(exchangeRate);
        let totalExchangeRate = (amountVal * exchangeRate).toFixed(2);      // toFixed will give two value after decimal
        // console.log(totalExchangeRate);

        const exchangeRateText = document.querySelector(".exchange-rate");
        exchangeRateText.innerHTML = `${amountVal} ${fromCurrency.value} = ${totalExchangeRate} ${toCurrency.value}`
    })
}

getBtn.addEventListener("click",(e)=>{
    e.preventDefault();
    getExchangeRate();
})
exchangeIcon.addEventListener("click",()=>{
    let tempCurrency;
    tempCurrency = fromCurrency.value;
    fromCurrency.value = toCurrency.value;
    toCurrency.value = tempCurrency;
})